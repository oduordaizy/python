
first = int(input("Enter the fisrt number: "))

second = int(input("Enter the Second number: "))

operation = input("Enter the mathematical Operation e.g. + , - , /, * ")

if (operation == '+'):
    result = first + second
    print(result)

if (operation == '*'):
    result = first * second
    print(result)

if (operation == '-'):
    result = first - second
    print(result)

if (operation == '/'):
    result = first / second
    print(result)


